void solve() {
    ll n, l, r;
    cin >> n >> l >> r;
    
    vector<ll> v= {l};
    ll maxi = l;
    for(int i=2; i<=n; i++) {
        ll x = l%i;
        if(x != 0) x = i - l%i;
        maxi = max(maxi, l+x);
        v.push_back(l+x);
    }

    if(maxi > r) no;
    else {
        yes;
        cout << v << endl;
    }
}

