void solve() {
    ll x,y,z, k;
    cin >> x >> y >> z >> k;

    ll a, b, c, ans = 0;
    for(ll i=1; i<=x; i++) {
        for(ll j=1; j<=y; j++) {
            if(k%(i*j)==0) {
                a = x-i+1;
                b = y-j+1;
                c = z - k/(i*j) + 1;
                if(a<=0 || b<=0 || c<=0) continue;
                ans = max(ans, a*b*c);
            }
        }
    }
    cout << ans << endl;
}

int32_t main()
{
    ios_base ::sync_with_stdio(false);
    cin.tie(NULL);
    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}
