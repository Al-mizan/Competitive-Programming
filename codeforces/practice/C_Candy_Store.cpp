void solve() {
    ll n;
    cin >> n;
    
    ll ans = 1, lcm = 1, mul = 1, gc = 0;
    while(n--) {
        ll a, b;
        cin >> a >> b;

        gc = gcd(gc, a*b);
        lcm = lcm*b / gcd(lcm, b);
        // debug(lcm);

        if(gc%lcm != 0) {
            ans++;
            gc = gcd(0, a*b);
            lcm = b / gcd(1, b);
        }
    }

    cout << ans << endl;
}

