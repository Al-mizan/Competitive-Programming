void solve() {
    string s;
    cin >> s;
    
    ll ways = 1, cnt = 1, ans = 0;
    for(int i = 0; i < s.size()-1;i++) {
        if(s[i] == s[i+1]) {
            cnt++;
            ans++;
            ways = ways * ans % mod;
        }
        else {
            ways = ways * cnt % mod;
            cnt = 1;
        }
    }
    ways = ways * cnt % mod;

    cout << ans << ' ' << ways << endl;
}


