void solve() {
    ll n, m, k;
    cin >> n >> m >> k;
    
    ll cnt = 0, ans = m;
    ll l = 1, r = m;
    while(l <= r) {
        ll mid = (l + r) / 2;
        cnt = n*(mid*(m/(mid+1)) + m%(mid+1));
        if(cnt >= k) {
            ans = mid;
            r = mid - 1;
        }
        else l = mid + 1;
    }
    cout << ans << endl;
}

